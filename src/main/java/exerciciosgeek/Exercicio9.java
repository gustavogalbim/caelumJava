package exerciciosgeek;

public class Exercicio9 {
    // Tipos de dado em Java - tipos numericos

    public static void main(String[] args) {

        // Por padrão os numeros reais em Java são considerados double
        // Para utilizar um float deve colocar um F no final

        // Tipos primitivos
        float preco1 = 23.4F;
        double preco2 = 23.4;

        // Tipo não primitivo
        Float preco3 = 44.5F;
        Double preco4 = 44.5;

        System.out.println("float -> preco1 = " +preco1);
        System.out.println("double -> preco2 = " +preco2);
        System.out.println("Float -> preco3 = " +preco3);
        System.out.println("Double -> preco4 = " +preco4);

    }
}
